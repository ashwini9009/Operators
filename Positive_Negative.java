package com.operators;

public class Positive_Negative {

	public static void main(String[] args) {
		int n = -31;
		if(n > 0 ) {
			System.out.println("Number is Positive " +n);	
		}
		else if(n<0)
		{
			System.out.println("Number is Negative " +n);
		}
		else {
			System.out.println("Number is zero");
		}
	}

}
